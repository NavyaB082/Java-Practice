package ContinueControlConstructs;

public class ContinueControlConstructs {

	public static void main(String[] args) {
		
		//Numbers
		for(int i=0;i<=10;i++) {
			if(i==5) {
				System.out.print("null"+" ");
				continue;
			}
			System.out.print(i+" ");
		}
		
		//skip an iteration

	}

}
